    private void refreshBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_refreshBtnActionPerformed
        try{
            totalNumAcc.setText(String.valueOf(names.size()));
            int current = 0;
            int savings = 0;
            for(int i = 0; i < accType.size(); i++){
                if(accType.get(i).matches("Current")){
                    current++;
                }

                else if(accType.get(i).matches("Savings")){
                    savings++;
                }

            }

            totalSavingsAcc.setText(String.valueOf(savings));
            totalCurrentAcc.setText(String.valueOf(current));

            branchTotal.setText(String.valueOf(statsTotal(accBranch, branchSpinner, "Total")));
            branchSavingTotal.setText(String.valueOf(statsTotal(accBranch, branchSpinner, "Savings")));
            branchCurrentTotal.setText(String.valueOf(statsTotal(accBranch, branchSpinner, "Current")));

            countyAccTotal.setText(String.valueOf(statsTotal(counties, countySpinner, "Total")));
            countySavingsTotal.setText(String.valueOf(statsTotal(counties, countySpinner, "Savings")));
            countyCurrentTotal.setText(String.valueOf(statsTotal(counties, countySpinner, "Current")));
            
            try{
                if(IDs.isEmpty()){
                    // Floats a bit tricky to work with
                    // When the refresh button is clicked and there are no entries in the array:
                    //  --> Simply set the text back to 0
                    statsMaxBalance.setText("0");
                    statsLowestBalance.setText("0");
                    statsBalanceAcc.setText("0");
                    statsAccBranch.setText("0");
                } // End if
                else{
                    // *** GENERAL STATISTICS ***
                    // Parse float to maxiumum 4 decimal places
                    // To increase or decrease decimal places change %.2f -> two decimal places
                    float average = Float.parseFloat(String.valueOf(IDs.size())) / Float.parseFloat(String.valueOf(branchSpinner.getItemCount()));
                    statsAccBranch.setText(String.valueOf(String.format("%.4f", average)));

                    int totalBalance = 0;
                    for(int i = 0; i < accBalance.size(); i++){
                        totalBalance += accBalance.get(i);
                    }

                    average = Float.parseFloat(String.valueOf(totalBalance)) / Float.parseFloat(String.valueOf(IDs.size()));
                    statsBalanceAcc.setText(String.valueOf(String.format("%.4f", average)));

                    statsMaxBalance.setText(String.valueOf(Collections.max(accBalance)));
                    statsLowestBalance.setText(String.valueOf(Collections.min(accBalance)));
                    
                }   // End else 
                
            }   // End try
            
            catch(Exception c){
                JOptionPane.showMessageDialog(rootPane, "Error: " + c);
            }   // End catch
            
        } // End try
        
        catch(Exception c){
            JOptionPane.showMessageDialog(rootPane, "Error: " + c);
        } // End catch

    }//GEN-LAST:event_refreshBtnActionPerformed

    private int statsTotal(ArrayList <String> arr, JComboBox box, String goal){
        int total = 0;
        try{
            switch(goal){
                case "Savings":
                    for(int i = 0; i < arr.size(); i++){
                        if(arr.get(i).matches(String.valueOf(box.getSelectedItem()))){
                            if(accType.get(i).matches(goal)){
                                total++;
                            }
                        }
                    }
                    break;

                case "Current":
                    for(int i = 0; i < arr.size(); i++){
                        if(arr.get(i).matches(String.valueOf(box.getSelectedItem()))){
                            if(accType.get(i).matches(goal)){
                                total++;
                            }
                        }
                    }
                    
                    break;

                case "Total":
                    for(int i = 0; i < arr.size(); i++){
                        if(arr.get(i).matches(String.valueOf(box.getSelectedItem()))){
                            total++;
                        }
                    }
                    break;

                default: 
                    return 0;

            }   // End switch
            
        }   // End try
        
        catch(Exception c){
            JOptionPane.showMessageDialog(rootPane, "Error: " + c);
        }   // End catch
        
        return total;
        
    }
    
