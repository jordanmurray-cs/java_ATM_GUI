    private void sendBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendBtnActionPerformed
        String emailBox = supportEmailBox.getText();
        String reg = "\\b[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\\.[A-Za-z]{2,4}\\b";

        Pattern pattern = Pattern.compile(reg);
        Matcher match = pattern.matcher(emailBox);
        
        try{
            if(match.find()) {
                JOptionPane.showMessageDialog(rootPane, "Hi " + String.valueOf(supportNameBox.getText()) + "\nYour support request has been sent");
                supportNameBox.setText("");
                supportEmailBox.setText("");
                supportQueryBox.setText("");
                supportDropdown.setSelectedIndex(0);
            } 

            else {
             JOptionPane.showMessageDialog(rootPane, "Invaild email: " + String.valueOf(supportEmailBox.getText()) + "\nPlease re-check and try again");
            }
        }
        catch(Exception c){
            JOptionPane.showMessageDialog(rootPane, "Error: " + c);
        }
    }//GEN-LAST:event_sendBtnActionPerformed

